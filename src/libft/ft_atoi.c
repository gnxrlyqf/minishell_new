#include <libft.h>

int	ft_atoi(const char *nptr)
{
	int		i;
	int		sign;
	long	res;

	i = 0;
	sign = 1;
	res = 0;
	while (nptr[i] == 32 || (nptr[i] >= 9 && nptr[i] <= 13))
		i++;
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		if (nptr[i] == '-')
			sign = -1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		if (res > (LONG_MAX - (nptr[i] - '0')) / 10)
			return (LONG_MAX * (sign > 0));
		res = res * 10 + nptr[i] - '0';
		i++;
	}
	return (res * sign);
}
