#include <libft.h>

void	ft_lstadd_front(t_list **lst, t_list *new)
{
	if (lst != NULL && new != NULL)
	{
		new->next = *lst;
		if (*lst != NULL)
			(*lst)->prev = new;
		new->prev = NULL;
		*lst = new;
	}
}
